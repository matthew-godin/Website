export function toPx(size) {
    return size.toString() + 'px';
};

export function toVh(size) {
    return size.toString() + 'vh';
};

export function toDesktopId(id) {
    return id + "-desktop";
}

export function toMobileId(id) {
    return id + "-mobile";
}

export function toIdLink(id) {
    return '#' + id;
}
